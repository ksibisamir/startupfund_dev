<p class=MsoNormal><b>Procédure</b></p>

<p class=MsoNormal>Le gouvernement tunisien a déclaré qu’un projet de loi sera
bientôt soumis à l’ARP, visant à réglementer le financement participatif. En
attendant la nouvelle loi, Ajyal Capital  a estimé que le besoin était urgent
et qu’il est possible d’utiliser des véhicules financiers existants et déjà
réglementés, tel que les “fonds communs de placement à risque (FCPR)” régis par
la réglementation depuis 2001 et qui permettent de financer des entreprises.</p>

<p class=MsoNormal>Sur la base de la réglementation en vigueur, Ajyal Capital
s’est ainsi proposé d\'émettre un FCPR pour servir de véhicule réglementaire à
tous les investisseurs. Un agrément a été demandé qui permet de commercialiser
le fonds à tous les investisseurs. Les Tunisiens qui le veulent peuvent ainsi
transformer une partie de leur épargne en financement d’entreprise et ce, en
bénéficiant, notamment des avantages fiscaux offerts par les FCPR et profiter
de la forte croissance de cette classe d’actifs.</p>

<p class=MsoNormal>Sachant qu’en Tunisie, les startups commencent à foisonner,
