financement par manque de vecteurs d’investissement spécialisés dans les
startups, Ajyal Capital a innové en proposant aux startups un moyen légal de se
financer à travers le financement participatif et ce, en passant par
l’intermédiaire d’un fonds d’investissement.</p>

